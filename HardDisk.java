package com;

public class HardDisk {
	int amount;
	float price;
	String brand;
	
	HardDisk(){
		this.amount=amount;
	}
	HardDisk(int amount,float price,String band){
		this.amount=amount;
		this.price=price;
		this.brand=brand;
	}
	
	
	public int getAmount() {
		return amount;
	}
	public void setAmount(int amount) {
		this.amount = amount;
	}

}
